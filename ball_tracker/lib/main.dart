// --- MODIFIED MODEL ---
class LevelResult {
  final int level;
  final double time;
  final bool wasCompleted;
  final Timestamp timestamp; // For sorting by time

  LevelResult({
    required this.level,
    required this.time,
    required this.wasCompleted,
    required this.timestamp,
  });

  // toJson now includes the timestamp
  Map<String, dynamic> toJson() => {
    'level': level,
    'time': time,
    'wasCompleted': wasCompleted,
    'timestamp': timestamp,
  };

  // fromJson now expects a Firestore Timestamp
  factory LevelResult.fromJson(Map<String, dynamic> json) {
    return LevelResult(
      level: json['level'],
      time: json['time'],
      wasCompleted: json['wasCompleted'],
      timestamp: json['timestamp'] as Timestamp, // Read as Timestamp
    );
  }
}

