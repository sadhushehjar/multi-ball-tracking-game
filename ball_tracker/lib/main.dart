// --- MODIFIED MODEL ---
class LevelResult {
  final int level;
  final double time; // Time in seconds (either tracking or reaction)
  final bool wasCompleted; // True if all targets were found
  final Timestamp timestamp; // For sorting by time

  // --- NEW FIELDS ---
  final bool gaveUp; // True if spacebar was pressed
  final bool wasIncorrect; // True if the wrong ball was clicked
  final int targetsInLevel; // How many targets they had to track
  final int targetsFound; // How many they found before failing/succeeding

  LevelResult({
    required this.level,
    required this.time,
    required this.wasCompleted,
    required this.timestamp,
    // --- NEW ---
    required this.gaveUp,
    required this.wasIncorrect,
    required this.targetsInLevel,
    required this.targetsFound,
  });

  // toJson now includes the new fields
  Map<String, dynamic> toJson() => {
    'level': level,
    'time': time,
    'wasCompleted': wasCompleted,
    'timestamp': timestamp,
    // --- NEW ---
    'gaveUp': gaveUp,
    'wasIncorrect': wasIncorrect,
    'targetsInLevel': targetsInLevel,
    'targetsFound': targetsFound,
  };

  // fromJson now expects the new fields
  // Added fallbacks (??) for backward compatibility with your old data
  factory LevelResult.fromJson(Map<String, dynamic> json) {
    return LevelResult(
      level: json['level'],
      time: json['time'],
      wasCompleted: json['wasCompleted'],
      timestamp: json['timestamp'] as Timestamp,
      // --- NEW ---
      gaveUp: json['gaveUp'] ?? false,
      wasIncorrect: json['wasIncorrect'] ?? false,
      targetsInLevel: json['targetsInLevel'] ?? 0,
      targetsFound: json['targetsFound'] ?? 0,
    );
  }
}

