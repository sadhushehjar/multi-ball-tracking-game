  };

  // --- RAG (Part A: Summarize Performance) ---
  Map<String, dynamic> _summarizePerformance() {
    if (widget.recentHistory.isEmpty) {
      return {'personalBest': widget.personalBest, 'status': 'New User'};
    }

    int giveUpCount = 0;
    int incorrectCount = 0;
    Map<int, int> levelAttempts = {};
    int recentMaxLevel = 0;

    for (final session in widget.recentHistory) {
      if (session.gaveUp) giveUpCount++;
      if (session.wasIncorrect) incorrectCount++;
      levelAttempts[session.level] = (levelAttempts[session.level] ?? 0) + 1;
      if (session.level > recentMaxLevel) {
        recentMaxLevel = session.level;
      }
    }

    String failureMode = 'none';
    if (giveUpCount > incorrectCount && giveUpCount > 3) {
      failureMode = 'gaveUp';
    } else if (incorrectCount > giveUpCount && incorrectCount > 3) {
      failureMode = 'wasIncorrect';
    }

    int stuckOnLevel = 0;
    for (final entry in levelAttempts.entries) {
      final level = entry.key;
      final attempts = entry.value;
      final hasCompleted = widget.recentHistory.any(
        (s) => s.level == level && s.wasCompleted,
      );
      if (!hasCompleted && attempts >= 3) {
        if (level > stuckOnLevel) stuckOnLevel = level;
      }
    }

    return {
      'personalBest': widget.personalBest,
      'recentSessionCount': widget.recentHistory.length,
      'recentMaxLevel': recentMaxLevel,
      'primaryFailureMode': failureMode,
      'stuckOnLevel': stuckOnLevel > 0 ? stuckOnLevel : 'none',
    };
  }

