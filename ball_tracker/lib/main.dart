class GameScreen extends StatefulWidget {
  // We now receive the unique Firebase User ID
  final String userId;

  const GameScreen({super.key, required this.userId});

  @override
  State<GameScreen> createState() => _GameScreenState();
}

class _GameScreenState extends State<GameScreen>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;

  // This holds our user's data from Firestore
  late DocumentReference _userDocRef;
  List<LevelResult> _userHistory = [];
  String _displayName = "Loading..."; // Default text while loading

  // (All game state variables remain the same)
  List<Ball> balls = [];
  bool isAnimating = false;
  bool isAwaitingClick = false;
  bool isButtonDisabled = false;
  String buttonText = "Start Level";
  String instructions = "Click 'Start' to begin. Watch the yellow balls.";
  final Size gameCanvasSize = const Size(350, 450);
  int level = 1;
  int totalBalls = 3;
  int ballsToTrack = 1;
  double speed = 2.0;
  int foundTargets = 0;
  int _personalBest = 1;
  final Stopwatch _trackingStopwatch = Stopwatch();
  final Stopwatch _reactionStopwatch = Stopwatch();
  Duration? _trackedDuration;

  static const Color targetColor = Color(0xFFFFC107);
  static const Color defaultColor = Color(0xFF007BFF);
  static const Color correctColor = Color(0xFF28A745);
  static const Color incorrectColor = Color(0xFFDC3545);

  @override
  void initState() {
    super.initState();
    _controller =
        AnimationController(vsync: this, duration: const Duration(seconds: 1))
          ..addListener(() {
            if (isAnimating) {
              setState(() {
                for (var ball in balls) {
                  ball.update(gameCanvasSize);
                }
              });
            }
          });

    // Set the reference to this user's document in Firestore
    _userDocRef = FirebaseFirestore.instance
        .collection('users')
        .doc(widget.userId);

    _loadUserData();
  }

  // --- NEW: Sign Out Method ---
  Future<void> _signOut() async {
    await FirebaseAuth.instance.signOut();
    // No navigation needed. The StreamBuilder in BallTrackerApp will handle it.
  }

  // --- MODIFIED: Loads from Firestore ---
  Future<void> _loadUserData() async {
    try {
      // 1. Load the user's main document
      final userSnapshot = await _userDocRef.get();

      if (!userSnapshot.exists) {
        // This shouldn't happen if sign-up is correct, but good to check
        await _userDocRef.set({'personalBest': 1});
      }

      final userData = userSnapshot.data() as Map<String, dynamic>?;

      // 2. Load the user's history from the subcollection
      final historySnapshot = await _userDocRef
          .collection('history')
          .orderBy('timestamp', descending: true) // Get most recent first
          .get();

      final history = historySnapshot.docs
          .map((doc) => LevelResult.fromJson(doc.data()))
          .toList();

      setState(() {
        _personalBest = userData?['personalBest'] ?? 1;
        _userHistory = history;
      });
    } catch (e) {
      // Handle error (e.g., show a snackbar)
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text("Error loading user data: $e")));
    }
  }

  // --- MODIFIED: Saves to Firestore Subcollection ---
  Future<void> _saveUserHistory(LevelResult result) async {
    try {
      // We add the new result to the 'history' subcollection
      // We use the model's toJson() method, which now includes the timestamp
      await _userDocRef.collection('history').add(result.toJson());

      // Add to local list to update UI immediately
      setState(() {
        _userHistory.insert(
          0,
          result,
        ); // Add to beginning (since we sort descending)
      });
    } catch (e) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text("Error saving history: $e")));
    }
  }

  // --- MODIFIED: Saves to Firestore Document ---
  Future<void> _savePersonalBest() async {
    if (level > _personalBest) {
      try {
        // Update the 'personalBest' field in the user's document
        await _userDocRef.update({'personalBest': level});
        setState(() {
          _personalBest = level;
        });
      } catch (e) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text("Error saving personal best: $e")),
        );
      }
    }
  }

  // --- MODIFIED: Uses Firestore data ---
  Future<void> _exportHistoryToCsv() async {
    if (_userHistory.isEmpty) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(const SnackBar(content: Text("No history to export.")));
      return;
    }

    final List<String> rowHeader = ['Level', 'Result', 'Time (s)', 'Timestamp'];
    List<List<String>> rows = [];
    rows.add(rowHeader);

    // _userHistory is already loaded from Firestore
    for (final result in _userHistory) {
      final List<String> row = [
        result.level.toString(),
        result.wasCompleted ? 'Answered' : 'Gave Up',
        result.time.toStringAsFixed(2),
        result.timestamp.toDate().toIso8601String(), // Add timestamp
      ];
      rows.add(row);
    }

    String csvData = rows.map((row) => row.join(',')).join('\n');

    final directory = await getTemporaryDirectory();
    // Use the Firebase UID for a unique filename
    final path = '${directory.path}/tracking_history_${widget.userId}.csv';
    final file = File(path);
    await file.writeAsString(csvData);

    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text("CSV file created. Opening share dialog...")),
    );

    await Share.shareXFiles([
      XFile(path),
    ], subject: 'Ball Tracking History for User ${widget.userId}');
  }

  // (Game logic methods: _setupLevel, _startAnimation, _handleTap,
  // _handleSpaceBarPress, _increaseDifficulty, _resetLevel, _revealTargets
  // are almost identical, just the _saveUserHistory call is modified)

  // ... (setup, startAnimation, etc. are the same) ...

  void _setupLevel() {
    setState(() {
      instructions = "Watch the ${ballsToTrack} yellow ball(s).";
      isButtonDisabled = true;
      isAwaitingClick = false;
      isAnimating = false;
      foundTargets = 0;
      _trackedDuration = null;
      _reactionStopwatch.reset();
      balls.clear();

      final random = Random();
      for (int i = 0; i < totalBalls; i++) {
        const radius = 15.0;
        final x =
            radius + random.nextDouble() * (gameCanvasSize.width - 2 * radius);
        final y =
            radius + random.nextDouble() * (gameCanvasSize.height - 2 * radius);
        final angle = random.nextDouble() * 2 * pi;
        final dx = cos(angle) * speed;
        final dy = sin(angle) * speed;
        final isTarget = i < ballsToTrack;
        final color = isTarget ? targetColor : defaultColor;

        balls.add(
          Ball(
            position: Offset(x, y),
            velocity: Offset(dx, dy),
            color: color,
            isTarget: isTarget,
          ),
        );
      }
    });

    Future.delayed(const Duration(milliseconds: 2500), () {
      if (!mounted) return;
      setState(() {
        for (var ball in balls) {
          ball.color = defaultColor;
        }
        instructions = 'Tracking... (Press SPACE if you lose track)';
      });
      _startAnimation();
    });
  }

  void _startAnimation() {
    isAnimating = true;
    _trackingStopwatch.reset();
    _trackingStopwatch.start();
    _controller.repeat();

    Future.delayed(const Duration(seconds: 6), () {
      if (!isAnimating || !mounted) return;
      _trackingStopwatch.stop();
      isAnimating = false;
      _controller.stop();

      _reactionStopwatch.reset();
      _reactionStopwatch.start();

      setState(() {
        isAwaitingClick = true;
        instructions =
            "Click the ${ballsToTrack - foundTargets} ball(s) you were tracking.";
      });
    });
  }

  void _handleTap(Offset tapPosition) {
    if (!isAwaitingClick) return;
    for (final ball in balls) {
      final distance = (tapPosition - ball.position).distance;
      if (distance < ball.radius) {
        if (ball.color == correctColor || ball.color == incorrectColor)
          continue;
        setState(() {
          if (ball.isTarget) {
            ball.color = correctColor;
            foundTargets++;
            if (foundTargets == ballsToTrack) {
              _reactionStopwatch.stop();
              double reactionTimeInSeconds =
                  _reactionStopwatch.elapsedMilliseconds / 1000.0;

              instructions = "Correct! Well done! ðŸŽ‰";
              isAwaitingClick = false;
              isButtonDisabled = false;
              buttonText = "Next Level";

              final result = LevelResult(
                level: level,
                time: reactionTimeInSeconds,
                wasCompleted: true,
                timestamp: Timestamp.now(), // Use current time
              );
              // --- MODIFIED CALL ---
              _saveUserHistory(result); // Pass result to save method
              _increaseDifficulty();
            } else {
              instructions =
                  "Good job! Find the remaining ${ballsToTrack - foundTargets} ball(s).";
            }
          } else {
            ball.color = incorrectColor;
            instructions = "Incorrect. Try this level again. ðŸ¤”";
            isAwaitingClick = false;
            _reactionStopwatch.stop();
            _revealTargets();
            _resetLevel();
          }
        });
        break;
      }
    }
  }

  void _handleSpaceBarPress() {
    if (!isAnimating) return;
    _trackingStopwatch.stop();
    _controller.stop();
    isAnimating = false;
    isAwaitingClick = false;

    setState(() {
      _trackedDuration = _trackingStopwatch.elapsed;
      double timeInSeconds = _trackedDuration!.inMilliseconds / 1000.0;

      final result = LevelResult(
        level: level,
        time: timeInSeconds,
        wasCompleted: false,
        timestamp: Timestamp.now(), // Use current time
      );
      // --- MODIFIED CALL ---
      _saveUserHistory(result); // Pass result to save method

      String formattedTime = timeInSeconds.toStringAsFixed(1);
      instructions = 'Tracked for ${formattedTime}s. Let\'s see the answer.';
      _revealTargets();
      _resetLevel();
    });
  }

  void _increaseDifficulty() {
    level++;
    _savePersonalBest(); // This will save to Firestore
    if (level % 2 == 0 && ballsToTrack < 5) {
      ballsToTrack++;
    }
    totalBalls++;
    if (level % 3 == 0) {
      speed += 0.25;
    }
  }

  void _resetLevel() {
    setState(() {
      isButtonDisabled = false;
      buttonText = "Try Again";
    });
  }

  void _revealTargets() {
    setState(() {
      for (final ball in balls) {
        if (ball.isTarget) {
          ball.color = targetColor;
        }
      }
    });
  }

