    options: DefaultFirebaseOptions.currentPlatform,
  );

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Ball Tracking Game',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        primarySwatch: Colors.blue,
        scaffoldBackgroundColor: const Color(0xFFF0F2F5),
        fontFamily: 'Roboto',
      ),
      // Use a FutureBuilder to wait for Firebase to initialize
      home: FutureBuilder(
        future: _initialization,
        builder: (context, snapshot) {
          // If there's an error (e.g., no internet, bad config)
          if (snapshot.hasError) {
            return Scaffold(
              body: Center(
                child: Text('Error initializing Firebase: ${snapshot.error}'),
              ),
            );
          }

          // If Firebase has initialized successfully, show your auth logic
          if (snapshot.connectionState == ConnectionState.done) {
            // This is YOUR existing StreamBuilder logic
            return StreamBuilder<User?>(
              stream: FirebaseAuth.instance.authStateChanges(),
              builder: (context, authSnapshot) {
                // This is the spinner for *auth*, not Firebase init
                if (authSnapshot.connectionState == ConnectionState.waiting) {
                  return const Scaffold(
                    body: Center(child: CircularProgressIndicator()),
                  );
                }

                // User is logged in
                if (authSnapshot.hasData) {
                  return GameScreen(userId: authSnapshot.data!.uid);
                }

                // User is logged out
                return const AuthScreen();
              },
            );
          }

          // While Firebase is initializing, show a loading spinner.
          // This FIXES the blank screen.
          return const Scaffold(
            body: Center(child: CircularProgressIndicator()),
          );
        },
      ),
    );
